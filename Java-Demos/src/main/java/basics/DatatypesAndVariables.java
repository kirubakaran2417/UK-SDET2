package basics;

public class DatatypesAndVariables {

    public static void main(String[] args) {

        //Datatypes have two types

        //1. Primitive
        //2. Non-Primitive

        //Primitive Datatypes-Built-in
        //byte
        //short
        //1. int->whole numbers like 10, 20 upto
        //2. long
        //3. float
        //4. double
        //5. boolean
        //6. char

        byte a=127;//maximum value
        short b=32767;//maximum value
        int c=2147483647;//maximum value
        long d=9223372036854775807L;//maximum value
        float e=1.2f;//maximum value
        double f=1.2;//maximum value
        boolean g=true;
        char h='a';

        //how to declare a variable
        int value;//declaration of variable
        value=10;//initialization of variable
        System.out.println("The value of variable is: "+value);//10


    }
}
