package conditionalStatements;

public class Loops {

    //why we are using loops

    public static void main(String[] args) {


        //while loop-top down loop
        //do while loop-bottom up loop
        //for loop-top down loop
        int i = 0;
        while (i<5){
            System.out.println("The value of i  is: " + i);
            i++;
        }
        System.out.println("The loop value become " + i + " so i came out of the loop");

    }
}
